// 6. Output in C
/*
    CASES
        1. integers
            printf("age is %d", age);
        2.  real numbers
            printf("value of pi is %f",pi);
        3.  characters
            printf("star looks like this %c",star);
*/
#include<stdio.h>
int main(){
    int age = 12;
    printf("age is %d", age);
    printf("\n", age);
    float pi = 3.14f;
    printf("value of pi is %f",pi);
    printf("\n", age);
    char star = '*';
    printf("star looks like this %c",star);
    printf("\n", age);
    return 0;
}